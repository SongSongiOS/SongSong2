import UIKit


extension UIColor {
    
    @nonobjc class var white10: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var mint10: UIColor {
        return UIColor(red: 174.0 / 255.0, green: 232.0 / 255.0, blue: 207.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var black10: UIColor {
        return UIColor(red: 0 / 255.0, green: 0 / 255.0, blue: 0 / 255.0, alpha: 1.0)
    }
}
